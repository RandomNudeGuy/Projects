def func(num1, num2):
    """calculates two numbers with addition
    :param num1: first number
    :param num2: second number
    :type num1: int
    :type num2: int
    :return: the addition of the two numbers
    :rtype: int"""
    print(num1 + num2)

def main():
    func(1, 2)

if __name__ == "__main__":
    main()